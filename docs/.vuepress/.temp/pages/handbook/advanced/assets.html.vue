  <h2 id="图标" tabindex="-1"
    ><a class="header-anchor" href="#图标" aria-hidden="true">#</a> 图标</h2
  >
  <p
    >脚手架针对图标的处理方案，有两种，一种是传统的 <code>iconfont</code> 方式，另外一种是
    <code>svg</code> 的方式。推荐使用 <code>svg</code> 的方式来处理图标。</p
  >
  <h3 id="iconfont-的不足" tabindex="-1"
    ><a class="header-anchor" href="#iconfont-的不足" aria-hidden="true">#</a> iconfont 的不足</h3
  >
  <ul>
    <li
      >浏览器将其视为文字进行抗锯齿优化，有时得到的效果并没有想象中那么锐利。尤其是在不同系统下对文字进行抗锯齿的算法不同，可能会导致显示效果有所差异。</li
    >
    <li
      >Icon Font 作为一种字体，Icon 显示的大小和位置可能要受到 font-size、line-height、word-spacing
      等等 CSS 属性的影响。 Icon 所在容器的 CSS 样式可能对 Icon
      的位置产生影响，调整起来很不方便。</li
    >
    <li
      >使用上存在不便。加载一个包含数百图标的 Icon
      Font，却只使用其中几个图标，非常浪费加载时间。通过 iconfont
      生成的图标，新增图标需要重新下载替换原有图标资源，不够灵活。</li
    >
    <li
      >为了实现最大程度的浏览器支持，可能要提供至少四种（TTF、WOFF、EOT、SVG）不同类型的字体文件。</li
    >
    <li>填充仅限于单色，无法支持包含多种颜色的图标。</li>
  </ul>
  <h3 id="svg-的优势" tabindex="-1"
    ><a class="header-anchor" href="#svg-的优势" aria-hidden="true">#</a> Svg 的优势</h3
  >
  <ul>
    <li>支持多色图标了，不再受单色限制。</li>
    <li>支持像字体那样通过 font-size, color 来调整样式。</li>
    <li>支持 ie9+。</li>
    <li>可利用 CSS 实现动画。</li>
    <li>Svg 雪碧图可以减少 HTTP 请求。</li>
    <li
      >矢量，缩放不失真，在高倍屏幕上更清晰，文件尺寸更小，能保证更快的加载时间以及更好的用户体验。</li
    >
    <li>可以很精细的控制 SVG 图标的每一部分。</li>
  </ul>
